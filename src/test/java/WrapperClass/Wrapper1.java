package WrapperClass;

public class Wrapper1 {
    public static void main(String[] args) {
        int a=10;
        Integer i=a;
        int b=i;
        System.out.println(i);
        System.out.println(b);
        short s=20;
        Short s1=s;
        System.out.println(s1);
        short s3=s1;
        System.out.println(s3);
    }
}
